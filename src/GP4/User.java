package GP4;

public class User {
    private String un;
    private String email;
    private String pwd;

    public User(){
    }

    public String getUn(){
        return un;
    }

    public void setUn(String value){
        un = value;
    }

    public String getEmail(){
        return email;
    }

    public void setEmail(String value){
        email = value;
    }

    public String getPwd(){
        return pwd;
    }

    public void setPwd(String value){
        pwd = value;
    }

}
